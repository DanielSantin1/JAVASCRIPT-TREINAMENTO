let x = 10;
let y = 5;

console.log(x + y);  // 15 (adição)
console.log(x > y);   // true (maior que)
console.log(x === 10);  // true (igualdade estrita)
let condition1 = x > y;  // true
let condition2 = y > x;  // false
console.log(condition1 && condition2);  // false (E lógico)

