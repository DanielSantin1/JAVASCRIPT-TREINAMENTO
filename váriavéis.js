let nome = "Maria"; // Variável que pode ser alterada
const idade = 25; // Constante que não pode ser alterada

console.log(nome); // Imprime "Maria"
console.log(idade); // Imprime 25

nome = "João"; // Reatribui o valor da variável nome, ou seja, JavaScript permite que variáveis sejam reatribuídas, mas constantes não.

console.log(nome); // Imprime "João"
// console.log(idade); // Isso causaria um erro, pois idade é uma constante
